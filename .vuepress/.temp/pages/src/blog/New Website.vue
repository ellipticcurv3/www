<h2 id="important-improvements"><a class="header-anchor" href="#important-improvements">#</a> Important Improvements</h2>
<h3 id="vuepress"><a class="header-anchor" href="#vuepress">#</a> VuePress</h3>
<p>Previously I used a combination of Jekyll, Webpack, Vue, a custom 'prerenderer' and some other 'tools' to build my website. Jekyll is based on Ruby, while Webpack is a Node application. A very awkward combination, because it works very difficult and was also very difficult to maintain.</p>
<p>Thanks to VuePress it is now no longer necessary to call multiple standalone applications. VuePress is the solution for a Vue-based system within Node, it is easy to maintain and comes with some nice extras by default.</p>
<p>You can think of support for multiple languages, a built-in prerendererer and the possibility to use Vue components within your blog posts. VuePress is very cool, works well and is well documented.</p>
<h3 id="css-based-masonry-layout"><a class="header-anchor" href="#css-based-masonry-layout">#</a> CSS-based Masonry-layout</h3>
<p>On the overview of my blog and portfolio I use a Masonry layout that is entirely based on CSS. Because of this, it does not depend on slow scripts and it's a lot faster.</p>
<h3 id="multiple-languages"><a class="header-anchor" href="#multiple-languages">#</a> Multiple languages</h3>
<p>You may have already noticed, it is now possible to view my website in both Dutch and English. I still have to write all content but there's a beginning.</p>
